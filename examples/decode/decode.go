package main

import (
	"fmt"

	"github.com/jwetzell/oca-go"
)

func main() {
	data := []byte{0x3b, 0x00, 0x01, 0x00, 0x00, 0x00, 0x2f, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x26, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0x00, 0x03, 0x00, 0x01, 0x05, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x04, 0x1f, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x01, 0x00, 0x00}

	var pdu oca.Ocp1MessagePdu
	err := pdu.UnmarshalBinary(data)
	if err != nil {
		fmt.Printf("failed to unmarshal: %s\n", err)
		return
	}
	fmt.Printf("successfully unmarshaled: %+v\n", pdu)
}
